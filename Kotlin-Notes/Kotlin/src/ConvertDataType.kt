

fun main(args:Array<String>) {

    // variable'sName.toInt() => convert to int
    var strInt:String = "123"
    var intStr:Int = strInt.toInt()
    println(strInt)


    // variable'sName.toDouble() => convert to double
    var strDouble:String = "10.5"
    var doubleStr:Double = strDouble.toDouble()
    println(doubleStr)


    // variable'sName.toString() => convert to string
    var intData:Int = 100
    var strData:String = intData.toString()
    println(strData)


    var doubleData2:Double = 18.9
    var intData2:Int = doubleData2.toInt()
    println(intData2)
}